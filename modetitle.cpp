//=============================================================================
// 描画処理
//=============================================================================
void DrawModetitle(void)
{



	// 2Dの物を描画する処理
		// Z比較なし
	SetDepthEnable(FALSE);

	// ライティングを無効
	SetLightEnable(FALSE);

	DrawTitle();


	// ライティングを有効に
	SetLightEnable(TRUE);

	// Z比較あり
	SetDepthEnable(TRUE);
}